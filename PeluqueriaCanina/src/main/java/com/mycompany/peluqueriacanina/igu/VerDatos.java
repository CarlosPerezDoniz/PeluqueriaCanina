    private void cargarTabla() {

        //Definir el modelo que queremos que tenga la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {

            //Para que las filas y columnas no sean editables directamente
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //Establecemos los nombres de las columnas
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alérgico", "At. Esp", "Dueño", "Móvil"};
        modeloTabla.setColumnIdentifiers(titulos);

        //carga de datos desde la base de datos
        List<Mascota> listaMascotas = control.traerMascotas();

        //recorre la lista y muestra cada uno de los elementos en la tabla
        if (listaMascotas != null) {
            for (Mascota masco : listaMascotas) {
                Object[] objeto = {masco.getNum_cliente(), masco.getNombre(), masco.getColor(), masco.getRaza(),
                    masco.getAlergico(), masco.getAtencion_especial(), masco.getUnDuenio().getNombre(), masco.getUnDuenio().getMovil()};

                modeloTabla.addRow(objeto);
            }
        }
        //asigno todo a la tabla de la interfaz gráfica
        tablaMascotas.setModel(modeloTabla);
    }
