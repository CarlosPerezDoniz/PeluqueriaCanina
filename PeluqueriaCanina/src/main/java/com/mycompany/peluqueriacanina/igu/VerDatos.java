    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //se controla que la tabla no esté vacia
        if (tablaMascotas.getRowCount() > 0) {
            //se controla que se haya seleccionado una mascota
            if (tablaMascotas.getSelectedRow() != -1) {
                //se obtiene la id de la mascota a eliminar
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)));

                //se llama al metodo borrar
                control.borrarMascota(num_cliente);

                //se muestra un mensaje indicando que se eliminó correctamente
                mostrarMensaje("Mascota eliminada correctamente", "Info", "Borrado de mascota");
                
                cargarTabla();

            } else {
                //se muestra mensaje de error al no seleccionar ninguna mascota
                mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al eliminar");
            }
        } else {
            //se muestra mensaje de error porque la base de datos está vacia
            mostrarMensaje("La lista está vacia", "Error", "Error al eliminar");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

